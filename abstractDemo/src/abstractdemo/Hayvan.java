package abstractdemo;

public abstract class Hayvan {
    protected String ad;
    protected int yas;
    
    public abstract void sesCikart();
    public abstract void beslen();

    /**
     * @return the ad
     */
    public String getAd() {
        return ad;
    }

    /**
     * @param ad the ad to set
     */
    public void setAd(String ad) {
        this.ad = ad;
    }

    /**
     * @return the yas
     */
    public int getYas() {
        return yas;
    }

    /**
     * @param yas the yas to set
     */
    public void setYas(int yas) {
        this.yas = yas;
    }
    
    
}
